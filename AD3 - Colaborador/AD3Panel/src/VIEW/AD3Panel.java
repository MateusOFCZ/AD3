    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Inicio Inicio = new Inicio();
        Desktop.add(Inicio);
        Inicio.setLocation(-5, -25);
        Inicio.setVisible(true);
        MODEL.AD3Panel AD3Panel = new MODEL.AD3Panel();
        boolean Coletado = AD3Panel.SetarInfos();
        if(Coletado == true){
            Menu_EMail.setText(AD3Panel.getEMail());
            Menu_NomeCompleto.setText(AD3Panel.getNomeCompleto());
        } else {
            Menu_EMail.setText("E-Mail não localizado...");
            Menu_NomeCompleto.setText("Nome não localizado...");
        }
    }//GEN-LAST:event_formWindowOpened

