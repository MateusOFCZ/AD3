    private void Botao_InicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Botao_InicioActionPerformed
        Inicio Inicio = new Inicio();
        JInternalFrame Frame = Desktop.getSelectedFrame();
        
        if(Frame == null) {
            Desktop.add(Inicio);
            Inicio.setLocation(-5, -25);
            Inicio.setVisible(true);
        } else {
            Frame.dispose();
            Desktop.add(Inicio);
            Inicio.setLocation(-5, -25);
            Inicio.setVisible(true);
        }
    }//GEN-LAST:event_Botao_InicioActionPerformed

